package main

import "fmt"

func main() {
	//stings in goLang can be added together with +
	fmt.Println("go" + "lang!!")

	//integers and floats in goLang
	fmt.Println("1=1 =", 1+1)
	fmt.Println("7.0/3.0 =", 7.0/3.0)

	//booleans with boolean operators
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
}
